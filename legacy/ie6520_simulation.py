import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim
import matplotlib.pyplot as plt

d = 5
T = 500
n_seeds = 5
threshold = 0.3
lr = 0.01

class Model(nn.Module):
    def __init__(self):
        super().__init__()
        self.linear = nn.Linear(d, 1)

    def forward(self, x):
        return self.linear(x).squeeze(-1)

def sample_action():
    return np.random.randn(d)

def sample_pref(x1, x2, theta):
    r1 = np.dot(theta, x1)
    r2 = np.dot(theta, x2)
    prob = 1 / (1 + np.exp(-(r1 - r2)))
    return 1 if np.random.rand() < prob else -1

def run(mode):
    histories = []

    for seed in range(n_seeds):
        np.random.seed(seed)
        torch.manual_seed(seed)

        theta_star = np.random.randn(d)
        model = Model()
        opt = optim.SGD(model.parameters(), lr=lr)

        correct_count = 0
        acc_history = []

        for t in range(T):
            x1 = sample_action()
            x2 = sample_action()

            x1_t = torch.tensor(x1, dtype=torch.float32)
            x2_t = torch.tensor(x2, dtype=torch.float32)

            r1 = np.dot(theta_star, x1)
            r2 = np.dot(theta_star, x2)

            optimal = 1 if r1 > r2 else -1

            s1 = model(x1_t)
            s2 = model(x2_t)
            diff = s1 - s2

            # ===== Mode =====
            if mode == "full":
                o = sample_pref(x1, x2, theta_star)

            elif mode == "active_pl":
                if abs(diff.item()) < threshold:
                    o = sample_pref(x1, x2, theta_star)
                else:
                    o = 1 if diff.item() > 0 else -1

            elif mode == "active_no_pl":
                if abs(diff.item()) < threshold:
                    o = sample_pref(x1, x2, theta_star)
                else:
                    o = 1 if diff.item() > 0 else -1

            # ===== Train =====
            loss = -torch.log(torch.sigmoid(torch.tensor(o, dtype=torch.float32) * (s1 - s2)))

            opt.zero_grad()
            loss.backward()
            opt.step()

            # ===== Prediction =====
            pred = 1 if model(x1_t) > model(x2_t) else -1

            if pred == optimal:
                correct_count += 1

            acc_history.append(correct_count / (t + 1))

        histories.append(acc_history)

    return np.mean(histories, axis=0)


# Run
