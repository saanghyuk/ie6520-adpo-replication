import numpy as np
import matplotlib.pyplot as plt

# ====== Settings ======
d = 5
T = 500
n_seeds = 10

# ====== Environment ======
def sigmoid(z):
    return 1 / (1 + np.exp(-z))

def sample_action():
    return np.random.randn(d)

def run_method(mode, theta_star, threshold=1.0):
    regrets = []
    queries = []

    for seed in range(n_seeds):
        np.random.seed(seed)
        theta_hat = np.zeros(d)
        lr = 0.01  # smaller learning rate for stability

        total_regret = 0
        q = 0

        for t in range(T):
            x1 = sample_action()
            x2 = sample_action()

            r1 = np.dot(theta_star, x1)
            r2 = np.dot(theta_star, x2)
            optimal = max(r1, r2)

            pred_diff = np.dot(theta_hat, x1 - x2)

            # ===== Mode logic =====
            if mode == "full":
                prob = sigmoid(r1 - r2)
                o = 1 if np.random.rand() < prob else -1
                q += 1

            elif mode == "active_pl":
                if abs(pred_diff) < threshold:
                    prob = sigmoid(r1 - r2)
                    o = 1 if np.random.rand() < prob else -1
                    q += 1
                else:
                    o = 1 if pred_diff > 0 else -1

            elif mode == "active_no_pl":
                if abs(pred_diff) < threshold:
                    prob = sigmoid(r1 - r2)
                    o = 1 if np.random.rand() < prob else -1
                    q += 1
                else:
                    chosen_reward = r1 if pred_diff > 0 else r2
                    total_regret += (optimal - chosen_reward)
                    continue

            # ===== Update =====
            grad = o * (x1 - x2)
            theta_hat += lr * grad

            # ===== Evaluate =====
            chosen_reward = r1 if np.dot(theta_hat, x1) > np.dot(theta_hat, x2) else r2
            total_regret += (optimal - chosen_reward)

        regrets.append(total_regret)
        queries.append(q)

    return np.mean(regrets), np.mean(queries)

