# Mini DPO-style experiment with active querying and pseudo-labels
# Runs on CPU (Colab compatible)

import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim
import matplotlib.pyplot as plt

# ====== 1. Settings ======
d = 5
T = 500
n_seeds = 5
threshold = 1.0
lr = 0.01

# ====== 2. Model ======
class SimpleModel(nn.Module):
    def __init__(self, d):
        super().__init__()
        self.linear = nn.Linear(d, 1)

    def forward(self, x):
        return self.linear(x).squeeze(-1)

# ====== 3. Environment ======
def sigmoid(z):
    return 1 / (1 + np.exp(-z))

def sample_action():
    return np.random.randn(d)

def sample_preference(x1, x2, theta_star):
    r1 = np.dot(theta_star, x1)
    r2 = np.dot(theta_star, x2)
    prob = sigmoid(r1 - r2)
    return 1 if np.random.rand() < prob else -1

# ====== 4. Training function ======
def run(mode):
    regrets = []
    queries = []

    for seed in range(n_seeds):
        np.random.seed(seed)
        torch.manual_seed(seed)

        theta_star = np.random.randn(d)

        model = SimpleModel(d)
        optimizer = optim.SGD(model.parameters(), lr=lr)

        total_regret = 0
        q = 0

        for t in range(T):
            x1 = sample_action()
            x2 = sample_action()

            x1_t = torch.tensor(x1, dtype=torch.float32)
            x2_t = torch.tensor(x2, dtype=torch.float32)

            r1 = np.dot(theta_star, x1)
            r2 = np.dot(theta_star, x2)
            optimal = max(r1, r2)

            s1 = model(x1_t)
            s2 = model(x2_t)
            diff = s1 - s2

            # ===== Mode logic =====
            if mode == "full":
                o = sample_preference(x1, x2, theta_star)
                q += 1

            elif mode == "active_pl":
                if abs(diff.item()) < threshold:
                    o = sample_preference(x1, x2, theta_star)
                    q += 1
                else:
                    o = 1 if diff.item() > 0 else -1

            elif mode == "active_no_pl":
                if abs(diff.item()) < threshold:
                    o = sample_preference(x1, x2, theta_star)
                    q += 1
                else:
                    chosen = r1 if diff.item() > 0 else r2
                    total_regret += (optimal - chosen)
                    continue

            # ===== DPO-style loss =====
            label = torch.tensor(o, dtype=torch.float32)
            loss = -torch.log(torch.sigmoid(label * (s1 - s2)))

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            # ===== Evaluate =====
            with torch.no_grad():
                s1_eval = model(x1_t)
                s2_eval = model(x2_t)
                chosen = r1 if s1_eval > s2_eval else r2

            total_regret += (optimal - chosen)

        regrets.append(total_regret)
        queries.append(q)

    return np.mean(regrets), np.mean(queries)

# ====== 5. Run ======
modes = ["full", "active_pl", "active_no_pl"]
results = {}

for m in modes:
    reg, q = run(m)
    results[m] = (reg, q)

# ====== 6. Plot ======
plt.figure()

plt.scatter(results["full"][1], results["full"][0], label="Full", marker="X", s=120)
plt.scatter(results["active_pl"][1], results["active_pl"][0], label="Active + PL", s=80)
plt.scatter(results["active_no_pl"][1], results["active_no_pl"][0], label="Active (no PL)", s=80)

plt.xlabel("Number of Queries")
plt.ylabel("Final Regret")
plt.title("Mini DPO-style Experiment")
